  <div class="uk-text-justify"> 
      <h3>Sistema de pontuação</h3>
      <table class="uk-table uk-table-condensed uk-table-striped" style="max-width:600px;">
        <tr>
          <td>Errar o vencedor do jogo e a quantidade de gols dos dois times</td>
          <td><b>0 ponto</b></td>
        </tr>
        <tr>
          <td>Acertar apenas a quantidade de gols de um dos times</td>
          <td><b>3 pontos</b></td>
        </tr>
        <tr>
          <td>Acertar apenas o vencedor (ou empate) do jogo</td>
          <td><b>10 pontos</b></td>
        </tr>
        <tr>
          <td>Acertar o vencedor do jogo e a quantidade de gols de um dos times</td>
          <td><b>14 pontos</b></td>
        </tr>
        <tr>
          <td>Acertar o placar exato do jogo</td>
          <td><b>25 pontos</b></td>
        </tr>
      </table>
      <p>
        Os pontos não são acumulativos, ou seja, em cada jogo o usuário irá receber 0, 3, 10, 14 ou 25 pontos. <br>
      </p>
      
    <p>
      O sistema de pontuação acima foi escolhido por propiciar disputas acirradas, onde a diferença entre dois participantes pode facilmente ficar em apenas 1 ponto. Outro fator que pesou na criação do sistema de pontuação foi a intenção de dar uma diferença considerável de pontos entre quem acerta o placar exato do jogo, e  quem acerta apenas o vencedor. Isso torna o sistema de pontuação mais justo com a dificuldade de acertar um placar exato e facilita recuperações de grandes diferenças.
    </p>
    <p>
      Em caso de empate na quantidade de pontos, os critérios de desempate serão os seguintes: Maior quantidade de placares exatos acertados; maior quantidade de vencedores acertados; cadastro mais antigo.
    </p>
  </div>
</div>

